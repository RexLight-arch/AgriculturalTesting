#define BH1750_I2C_ADDRESS (0x23 << 1) // 注意这里需要左移一位以适配7位地址


uint8_t data[2];  // 用于存储从BH1750接收的数据
float lux = -1;   // 用于保存最新的光照强度值

// 初始化BH1750传感器
void BH1750_Init(void)
{
    uint8_t cmd = 0x01;  // Power On
	 HAL_StatusTypeDef status ;
    if (HAL_I2C_Master_Transmit(&hi2c2, BH1750_I2C_ADDRESS, &cmd, 1, 500) != HAL_OK)
    {
        // 处理错误
        printf("error BH1750_init!\n");
    }
		// 可选：等待一段时间让设备稳定
    HAL_Delay(10);

    // 检查设备是否正常响应
    uint8_t test_data;
    status = HAL_I2C_Master_Receive(&hi2c2, BH1750_I2C_ADDRESS, &test_data, 1, 500);
    
    if (status != HAL_OK)
    {
        // 设备未响应
        printf("error inittwo!\n");
    }
}

// 设置测量模式为高分辨率模式
void BH1750_Set_High_Resolution_Mode(void)
{
    uint8_t cmd = 0x10;  // High-resolution mode (1lx resolution, 120ms measurement time)
    if (HAL_I2C_Master_Transmit(&hi2c2, BH1750_I2C_ADDRESS, &cmd, 1, 500) != HAL_OK)
    {
        // 处理错误
        printf("error high_mode!\n");
    }
}

// 启动读取光照强度值（使用中断方式）
void start_read_BH1750(void)
{
    BH1750_Set_High_Resolution_Mode();
    HAL_Delay(120);  // 根据分辨率等待必要的测量时间（120ms）

    if (HAL_I2C_Master_Receive_IT(&hi2c2, BH1750_I2C_ADDRESS, data, 2) != HAL_OK)
    {
        // 处理错误情况
        Error_Handler();
    }
}
